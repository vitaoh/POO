package POO1.exercicios.lista2013.ex228;

public class Contador {
    
    private int valor;

    public Contador() {
        valor = 0;
    }
    
    public void incrementar() {
        valor++;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int v) {
        valor = v;
    }

}
