package POO1.exercicios.campos2012.cap5.ex5;

public class EX5 {
    public static void main(String[] args) {
        System.out.println();
        for (int i = 1; i <= 10; i++) {
            for (int j = 1; j <= 10; j++) {
                System.out.printf("%4d", i * j);
            }
            System.out.println();
        }
        System.out.println();
    }
}