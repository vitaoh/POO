package POO1.prova.outraRevisao.ex4;

public class Calculadora {

    public static int somar(int n1, int n2) {
        return n1 + n2;
    }
    
    public static double somar(double n1, double n2) {
        return n1 + n2;
    }
    
    public static int somar(int n1, int n2, int n3) {
        return n1 + n2 + n3;
    }
}
