package POO1.aulas.associacao.composicao;

public class Roda {
    
    private boolean girando; 
    
    public Roda() {
        girando = false;
    }
    
    public boolean estaGirando() {
        return girando;
    }
    
    public void setGirando(boolean g) {
        girando = g;
    }
    
}

